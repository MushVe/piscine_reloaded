int	ft_sqrt(int nb)
{
	float	x;
	float	y;

	x = nb;
	y = 1;
	if (nb < 0)
		return (0);
	while ((int)x - (int)y)
	{
		x = (x + y) / 2;
		y = nb / x;
	}
	if (((int)x * (int)x) == nb)
		return (x);
	return (0);
}
